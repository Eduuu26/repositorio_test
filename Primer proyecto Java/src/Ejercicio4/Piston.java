package Ejercicio4;

public class Piston {
private String idPiston;
private String marca;

//

public Piston(String idPiston, String marca) {
	super();
	this.idPiston = idPiston;
	this.marca = marca;
}


public String getIdPiston() {
	return idPiston;
}


public void setIdPiston(String idPiston) {
	this.idPiston = idPiston;
}

public String getMarca() {
	return marca;
}

public void setMarca(String marca) {
	this.marca = marca;
}


	}


